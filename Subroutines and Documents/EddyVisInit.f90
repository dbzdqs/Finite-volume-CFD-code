!*********************************************************************************************
 Subroutine EddyVisInit(Dim,NC,Mu0,Mut0,Mut)
 Implicit None
!*********************************************************************************************
 Intent(In   )::Dim,NC,Mu0
 Intent(Out  )::Mut0,Mut

 Integer::Dim,J,NC
 Real(8)::Mu0,Mut0
 Real(8),Dimension(1:Dim)::Mut
!*********************************************************************************************	
!Part 1:
 Mut0 = 0.1*Mu0
 	
!Part 2:
 Do J=1,NC
    Mut(J) = Mut0
 End Do
!*********************************************************************************************
 End
