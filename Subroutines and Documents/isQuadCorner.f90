Function isQuadCorner(point,newQuad)
Implicit None
!===========================================================================================
Intent(In)::point,newQuad

Integer::point,I
Integer,Dimension(1:4)::newQuad
Logical::isQuadCorner
!===========================================================================================
!Part 1:
isQuadCorner = .false.
do I=1,4
	if(newQuad(I)==point) then
		isQuadCorner = .true.
		exit
	endif
end do
!===========================================================================================
End Function isQuadCorner
