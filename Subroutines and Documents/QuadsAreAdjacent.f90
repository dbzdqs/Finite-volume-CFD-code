Function QuadsAreAdjacent(Dim,Neib,Q1,Q2)
Implicit None
!===========================================================================================
Intent(In)::Dim,Neib,Q1,Q2

Integer::Dim,Q1,Q2,I
Integer,Dimension(1:Dim,1:4)::Neib
Logical::QuadsAreAdjacent
!===========================================================================================
!Part 1:
QuadsAreAdjacent = .False.
do I=1,4
    if(Neib(Q1,I) == Q2) then
        QuadsAreAdjacent = .True.
        exit
    endif
end do
!===========================================================================================
End Function QuadsAreAdjacent
