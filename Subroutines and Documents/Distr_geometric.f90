!*********************************************************************************************
SUBROUTINE Distr_geometric(N,Lambda,Spacinge)
 Implicit None
!*********************************************************************************************
 INTEGER::N,I,J
 Real*8,Dimension(1:N+1)::Spacinge,X
 Real*8::Lambda
!*********************************************************************************************
!part1
 Do I=1,N+1
    X(I)=I-1
 End Do
 
!part2
 Do J=1,N+1
    Spacinge(J)=1-((1-Lambda)**X(J))
 End Do
!*********************************************************************************************
 End
