Subroutine addToList(List,Count,Item)
Implicit None
!===========================================================================================
Intent(In)::Item
Intent(Out)::List,Count

Integer::Item,Count
Integer,Dimension(1:1000)::List
Logical::IsInTheList
!===========================================================================================
!Part 1:
if(.Not. IsInTheList(List,Count,Item)) then

	Count = Count + 1
	List(Count) = Item

endif
!===========================================================================================
End Subroutine addToList
