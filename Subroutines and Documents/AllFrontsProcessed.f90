Function AllFrontsProcessed(Dim,Fronts,States)
Implicit None
!===========================================================================================
Intent(In)::Dim,Fronts,States

Integer::Dim,Fronts,I
Integer,Dimension(1:Dim)::States
Logical::AllFrontsProcessed
!===========================================================================================
!Part 1:
AllFrontsProcessed = .True.
do I=1,Fronts
	if(States(I) /= -1) then
		AllFrontsProcessed = .False.
		exit
	endif
end do
!===========================================================================================
End Function AllFrontsProcessed
