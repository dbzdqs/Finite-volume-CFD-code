Function isInDeletingPointsList(DPL,DPL_Count,Point)
Implicit None
!===========================================================================================
Intent(In)::DPL,DPL_Count,Point

Integer::DPL_Count,Point,I
Integer,Dimension(1:1000)::DPL
Logical::isInDeletingPointsList
!===========================================================================================
isInDeletingPointsList = .False.
do I=1,DPL_Count
	if(DPL(I) == Point) then
		isInDeletingPointsList = .True.
		exit
	endif	
end do
!===========================================================================================
End Function isInDeletingPointsList
