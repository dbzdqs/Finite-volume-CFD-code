Function hasCorner(Dim,Corn,E,P)
Implicit None
!===========================================================================================
Intent(In)::Dim,Corn,E,P

Integer::Dim,E,P,I
Integer,Dimension(1:Dim,1:4)::Corn
Logical::hasCorner
!===========================================================================================
!Part 1:
hasCorner = .False.
do I=1,4
	if(Corn(E,I)==P) then
		hasCorner = .True.
		exit	
	endif
end do
!===========================================================================================
End Function hasCorner
