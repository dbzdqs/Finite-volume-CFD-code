Function IsInTheList(SC,SC_Count,point)
Implicit None
!===========================================================================================
Intent(In)::SC,SC_Count,point

Integer::SC_Count,point,I
Integer,Dimension(1:1000)::SC
Logical::IsInTheList
!===========================================================================================
!Part 1:
IsInTheList = .False.
do I=1,SC_Count
	if(SC(I) == point) then
		IsInTheList = .True.
		exit
	endif	
end do
!===========================================================================================
End Function IsInTheList
